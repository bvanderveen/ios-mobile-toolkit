
#import "RFColor.h"

@implementation RFColor

+ (UIColor *)lightGray
{
    return [UIColor colorWithWhite:0.598 alpha:1];
}

+ (UIColor *)mediumGray
{
    return [UIColor colorWithRed:0.4212 green:0.4291 blue:0.437 alpha:1];
}

+ (UIColor *)darkGray
{
    return [UIColor colorWithWhite:0.1496 alpha:1];
}

+ (UIColor *)lightBlue
{
    return [UIColor colorWithRed:0.263 green:0.665 blue:0.858 alpha:1];
}

+ (UIColor *)darkBlue
{
    return [UIColor colorWithRed:0.11 green:0.46 blue:0.708 alpha:1];
}

@end
